package A1_기본이론;
/*
 * 
이스케이프 문자(escape sequence)
1) \n	: 줄바꿈
2) \t	: tab
3) \"	: "
4) \'	: '

 */
public class 기본이론9_이스케이프문자 {
	public static void main(String[] args) {

		System.out.println("안녕\n하세요.");
		System.out.println("----------------------");
		System.out.println("안녕\t하세요.");
		System.out.println("----------------------");
		System.out.println("\"안녕하세요.\"");
		System.out.println("----------------------");
		System.out.println("\'안녕하세요.\'");
		System.out.println("----------------------");

	}
}